<h3>Tablice - wprowadzenie</h3>
<p>Tablice to zmienne które mogą przechowywać wiele wartości.</p>
<pre><code class="language-php">$cars = array("Volvo", "BMW", "Toyota");</code></pre>
<p>Powyższy kod tworzy tablicę z 3 samochodami.</p>
<p>W php możemy wyróżnić 3 rodzaje tablic:
    <ul>
        <li>tablice indeksowane - do kolejnych wartości odwołujemy się poprzez numer indeksu</li>
        <li>tablice asocjacyjne - do wartości odwołujemy się po nazwie klucza</li>
        <li>tablice wielowymiarowe - tablice zawierające w sobie tablice jako wartości</li>
    </ul>
</p>
<p>Wartości w tablicy mogą być dowolne, najczęściej są to liczby, ciągi znaków i inne proste zmienne, ale można przechowywać także np. obiekty czy funkcje</p>
<p>Funkcja count() zwraca ilość elementów w tablicy.</p>
<p>Zadanie: W kodzie masz dostęp do zmiennej $tablica, wyświetl jej długość</p>