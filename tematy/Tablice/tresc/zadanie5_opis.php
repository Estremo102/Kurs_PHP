<h3>Dodawanie elementów do tablicy</h3>
<p>Możemy na kilka sposobów dodawać elementy do tablicy</p>
<ul>
    <li>[] - dodaje pojedynczy element na końcu tablicy</li>
    <li>array_push() - dodaje jeden lub więcej elementów na końcu tablicy</li>
    <li>array_unshift() - dodaje jeden lub więcej elementów na początku tablicy</li>
    <li>array_splice() - usuwa część tablicy i zastępuje ją nowymi elementami</li>
    <li>array_merge() - łączy dwie lub więcej tablic</li>
</ul>
